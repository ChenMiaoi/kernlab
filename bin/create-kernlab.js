#!/usr/bin/env node
'use strict';

const fs = require('fs');
const path = require('path');
const { spawnSync } = require('child_process');

const PACKAGE_ROOT = path.resolve(__dirname, '..');
const PACKAGE_JSON = require('../package.json');
const SUBMODULES = require('../package/submodules.json');

const HIDDEN_TEMPLATE_DIR = '.kernlab';
const CODEX_DIR = '.codex';
const MANAGED_SKILLS = ['kernlab-repo-guardrails'];
const DEFAULT_SUPPORT_SUBMODULES = 'busybox qemu';
const GENERATED_MARKER = '# Generated by create-kernlab.';

const color = {
  reset: '\x1b[0m',
  bold: '\x1b[1m',
  cyan: '\x1b[36m',
  green: '\x1b[32m',
  yellow: '\x1b[33m',
  red: '\x1b[31m',
  dim: '\x1b[2m'
};

function paint(value, code) {
  return process.stdout.isTTY ? `${code}${value}${color.reset}` : value;
}

function usage() {
  console.log(`Usage: create-kernlab [workspace] [options]

Create a Linux-first QEMU kernel development workspace.

Default layout in the workspace root:
  linux/              Linux source tree users edit
  .kernlab/           QEMU/BusyBox/build tooling copied from this npm package
  qemu-linux.mk       workspace-local build/runtime config
  Makefile            wrapper; run 'make run' from the workspace root

Options:
  --dir <path>                 Workspace directory (default: current directory)
  --linux-dir <path>           Existing Linux source tree to use without modifying it
  --workspace-linux-dir <path> Clone Linux there instead of <workspace>/linux
  --linux-url <url>            Linux Git URL (default: packaged template metadata)
  --linux-branch <ref>         Linux branch/tag/ref to clone instead of packaged commit
  --support-submodules <list>  Support repos to initialize in tooling dir (default: "${DEFAULT_SUPPORT_SUBMODULES}")
  --submodule-depth <n>        Clone/fetch depth; use 0 for full history (default: 1)
  --template-url <url>         Clone tooling from a Git repo instead of packaged files
  --template-branch <ref>      Branch/tag/ref for --template-url
  --update                     Refresh .kernlab tooling in an existing workspace
  --dry-run                    Print the plan without creating files or cloning repos
  --yes                        Accepted for scripting; current flow is non-interactive
  --help                       Show this help
  --version                    Show package version

Environment aliases:
  INSTALL_DIR, LINUX_DIR, WORKSPACE_LINUX_DIR, LINUX_URL, LINUX_BRANCH,
  SUPPORT_SUBMODULES, SUBMODULE_DEPTH, REPO_URL, BRANCH

Examples:
  npx create-kernlab
  npx create-kernlab ~/src/my-kernel
  npx create-kernlab --linux-dir /home/me/src/linux
  npx create-kernlab --linux-url https://github.com/me/linux.git --linux-branch my-topic
`);
}

function parseArgs(argv) {
  const options = {
    workspace: process.env.INSTALL_DIR || '',
    linuxDir: process.env.LINUX_DIR || '',
    workspaceLinuxDir: process.env.WORKSPACE_LINUX_DIR || '',
    linuxUrl: process.env.LINUX_URL || '',
    linuxBranch: process.env.LINUX_BRANCH || '',
    supportSubmodules: process.env.SUPPORT_SUBMODULES ?? process.env.SUBMODULES ?? DEFAULT_SUPPORT_SUBMODULES,
    submoduleDepth: process.env.SUBMODULE_DEPTH ?? '1',
    templateUrl: process.env.REPO_URL || '',
    templateBranch: process.env.BRANCH || '',
    dryRun: false,
    update: false
  };

  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    const next = () => {
      i += 1;
      if (i >= argv.length) {
        throw new Error(`${arg} requires a value`);
      }
      return argv[i];
    };

    switch (arg) {
      case '--help':
      case '-h':
        usage();
        process.exit(0);
        break;
      case '--version':
      case '-v':
        console.log(PACKAGE_JSON.version);
        process.exit(0);
        break;
      case '--dir':
      case '--workspace':
        options.workspace = next();
        break;
      case '--linux-dir':
        options.linuxDir = next();
        break;
      case '--workspace-linux-dir':
        options.workspaceLinuxDir = next();
        break;
      case '--linux-url':
        options.linuxUrl = next();
        break;
      case '--linux-branch':
        options.linuxBranch = next();
        break;
      case '--support-submodules':
      case '--submodules':
        options.supportSubmodules = next();
        break;
      case '--submodule-depth':
        options.submoduleDepth = next();
        break;
      case '--template-url':
      case '--repo-url':
        options.templateUrl = next();
        break;
      case '--template-branch':
      case '--branch':
        options.templateBranch = next();
        break;
      case '--update':
        options.update = true;
        break;
      case '--dry-run':
        options.dryRun = true;
        break;
      case '--yes':
      case '-y':
        break;
      default:
        if (arg.startsWith('-')) {
          throw new Error(`Unknown option: ${arg}`);
        }
        if (options.workspace) {
          throw new Error(`Unexpected positional argument: ${arg}`);
        }
        options.workspace = arg;
        break;
    }
  }

  options.workspace = abs(options.workspace || '.');
  options.templateDir = path.join(options.workspace, HIDDEN_TEMPLATE_DIR);
  options.linuxDirExplicit = Boolean(options.linuxDir);
  if (options.linuxDirExplicit) {
    options.linuxDir = abs(options.linuxDir);
    options.workspaceLinuxDir = options.linuxDir;
    options.configLinuxDir = options.linuxDir;
  } else if (options.workspaceLinuxDir) {
    options.workspaceLinuxDir = abs(options.workspaceLinuxDir);
    options.configLinuxDir = options.workspaceLinuxDir;
  } else {
    options.workspaceLinuxDir = path.join(options.workspace, 'linux');
    options.configLinuxDir = '$(CURDIR)/linux';
  }
  options.linuxUrl = options.linuxUrl || SUBMODULES.linux.url;

  return options;
}

function abs(value) {
  return path.resolve(process.cwd(), value.replace(/^~(?=$|\/)/, process.env.HOME || '~'));
}

function run(cmd, args, options = {}) {
  if (options.dryRun) {
    console.log(`${paint('dry-run', color.yellow)} ${cmd} ${args.map(shellWord).join(' ')}`);
    return;
  }
  const result = spawnSync(cmd, args, { stdio: 'inherit', cwd: options.cwd || process.cwd(), env: options.env || process.env });
  if (result.status !== 0) {
    throw new Error(`${cmd} failed with exit code ${result.status}`);
  }
}

function shellWord(value) {
  return /^[A-Za-z0-9_/:=.,@%+-]+$/.test(value) ? value : JSON.stringify(value);
}

function ensureGit() {
  const result = spawnSync('git', ['--version'], { stdio: 'ignore' });
  if (result.status !== 0) {
    throw new Error('Missing required command: git');
  }
}

function isEmptyDir(dir) {
  if (!fs.existsSync(dir) || !fs.statSync(dir).isDirectory()) {
    return false;
  }
  return fs.readdirSync(dir).length === 0;
}

function isTemplateDir(dir) {
  return fs.existsSync(path.join(dir, 'Makefile')) &&
    fs.existsSync(path.join(dir, 'qemu-linux.mk')) &&
    fs.existsSync(path.join(dir, 'scripts', 'build-kernel.sh'));
}

function sameGeneratedFile(file, content) {
  return fs.existsSync(file) && fs.statSync(file).isFile() && fs.readFileSync(file, 'utf8') === content;
}

function hasGeneratedMarker(file) {
  return fs.existsSync(file) &&
    fs.statSync(file).isFile() &&
    fs.readFileSync(file, 'utf8').startsWith(GENERATED_MARKER);
}

function writeGeneratedOrMarked(pathname, content, dryRun) {
  if (dryRun) {
    console.log(`${paint('dry-run', color.yellow)} write ${pathname}`);
    return;
  }
  if (fs.existsSync(pathname) && !sameGeneratedFile(pathname, content) && !hasGeneratedMarker(pathname)) {
    failExisting(`Refusing to overwrite changed or unrelated generated file: ${pathname}`);
  }
  fs.writeFileSync(pathname, content);
}

function failExisting(message) {
  throw new Error(`${message}\nChoose another workspace/Linux directory, run with --update for an existing kernlab workspace, or move the existing path yourself before retrying.`);
}

function validateWorkspace(options, generatedMakefile, generatedConfig) {
  if (!fs.existsSync(options.workspace)) {
    return;
  }
  if (!fs.statSync(options.workspace).isDirectory()) {
    failExisting(`Workspace exists and is not a directory: ${options.workspace}`);
  }
  if (isEmptyDir(options.workspace)) {
    return;
  }

  for (const name of fs.readdirSync(options.workspace)) {
    const entry = path.join(options.workspace, name);
    if (name === HIDDEN_TEMPLATE_DIR) {
      if (!isTemplateDir(entry)) {
        failExisting(`Existing ${HIDDEN_TEMPLATE_DIR} is not kernlab tooling: ${entry}`);
      }
    } else if (name === 'linux') {
      if (path.resolve(entry) === path.resolve(options.workspaceLinuxDir)) {
        if (isEmptyDir(entry) || options.linuxDirExplicit) {
          continue;
        }
        failExisting(`Existing workspace linux/ is non-empty. Set --linux-dir ${entry} to use it explicitly.`);
      }
      failExisting(`Unexpected existing linux directory: ${entry}`);
    } else if (name === 'Makefile') {
      if (!sameGeneratedFile(entry, generatedMakefile)) {
        failExisting(`Refusing to overwrite changed or unrelated generated file: ${entry}`);
      }
    } else if (name === 'qemu-linux.mk') {
      if (!sameGeneratedFile(entry, generatedConfig)) {
        failExisting(`Refusing to overwrite changed or unrelated generated file: ${entry}`);
      }
    } else if (name !== 'out') {
      failExisting(`Workspace contains an unrelated existing path: ${entry}`);
    }
  }
}

function validateUpdateWorkspace(options) {
  if (!fs.existsSync(options.workspace) || !fs.statSync(options.workspace).isDirectory()) {
    throw new Error(`Workspace does not exist or is not a directory: ${options.workspace}`);
  }
  if (!fs.existsSync(options.templateDir) || !fs.statSync(options.templateDir).isDirectory()) {
    throw new Error(`Workspace has no ${HIDDEN_TEMPLATE_DIR} tooling directory: ${options.templateDir}`);
  }
  if (!isTemplateDir(options.templateDir)) {
    failExisting(`Existing ${HIDDEN_TEMPLATE_DIR} is not kernlab tooling: ${options.templateDir}`);
  }
}

function cloneRepo(url, dest, ref, depth, dryRun) {
  const args = ['clone'];
  if (depth && depth !== '0') {
    args.push('--depth', depth);
  }
  if (ref) {
    args.push('--branch', ref);
  }
  args.push(url, dest);
  run('git', args, { dryRun });
}

function fetchCommit(url, dest, commit, depth, dryRun) {
  if (dryRun) {
    console.log(`${paint('dry-run', color.yellow)} git init ${shellWord(dest)}`);
    console.log(`${paint('dry-run', color.yellow)} git -C ${shellWord(dest)} fetch origin ${commit}`);
    return;
  }
  fs.mkdirSync(path.dirname(dest), { recursive: true });
  if (!fs.existsSync(path.join(dest, '.git'))) {
    if (fs.existsSync(dest) && !isEmptyDir(dest)) {
      failExisting(`Git destination is non-empty and not initialized by this tool: ${dest}`);
    }
    run('git', ['init', dest]);
    run('git', ['-C', dest, 'remote', 'add', 'origin', url]);
  } else {
    run('git', ['-C', dest, 'remote', 'set-url', 'origin', url]);
  }
  const fetchArgs = ['-C', dest, 'fetch'];
  if (depth && depth !== '0') {
    fetchArgs.push('--depth', depth);
  }
  fetchArgs.push('origin', commit);
  run('git', fetchArgs);
  run('git', ['-C', dest, 'checkout', '--detach', 'FETCH_HEAD']);
}

function copyPackageTemplateFiles(options, force) {
  if (options.dryRun) {
    console.log(`${paint('dry-run', color.yellow)} ${force ? 'refresh' : 'copy'} package template into ${options.templateDir}`);
    return;
  }

  if (!force && fs.existsSync(options.templateDir) && !isEmptyDir(options.templateDir)) {
    return;
  }

  fs.mkdirSync(options.templateDir, { recursive: true });
  for (const item of ['Makefile', 'qemu-linux.mk', '.gitmodules', 'README.md', 'docs', 'scripts']) {
    const src = path.join(PACKAGE_ROOT, item);
    const dst = path.join(options.templateDir, item);
    fs.cpSync(src, dst, { recursive: true, force: true, preserveTimestamps: true });
  }
  writeWorkspaceSupportScript(options);
}

function updateTemplateRepo(options) {
  if (!fs.existsSync(options.templateDir)) {
    cloneRepo(options.templateUrl, options.templateDir, options.templateBranch, options.submoduleDepth, options.dryRun);
    return;
  }
  if (!fs.existsSync(path.join(options.templateDir, '.git'))) {
    failExisting(`Existing ${HIDDEN_TEMPLATE_DIR} is not a git checkout: ${options.templateDir}`);
  }
  if (options.dryRun) {
    console.log(`${paint('dry-run', color.yellow)} git -C ${shellWord(options.templateDir)} remote set-url origin ${shellWord(options.templateUrl)}`);
    console.log(`${paint('dry-run', color.yellow)} git -C ${shellWord(options.templateDir)} fetch origin ${shellWord(options.templateBranch || 'HEAD')}`);
    return;
  }
  run('git', ['-C', options.templateDir, 'remote', 'set-url', 'origin', options.templateUrl]);
  const fetchArgs = ['-C', options.templateDir, 'fetch'];
  if (options.submoduleDepth && options.submoduleDepth !== '0') {
    fetchArgs.push('--depth', options.submoduleDepth);
  }
  fetchArgs.push('origin', options.templateBranch || 'HEAD');
  run('git', fetchArgs);
  run('git', ['-C', options.templateDir, 'checkout', '--detach', 'FETCH_HEAD']);
}

function copyTemplateFiles(options, force = false) {
  if (options.templateUrl) {
    if (force) {
      updateTemplateRepo(options);
    } else if (!fs.existsSync(options.templateDir)) {
      cloneRepo(options.templateUrl, options.templateDir, options.templateBranch, options.submoduleDepth, options.dryRun);
    }
    return;
  }

  copyPackageTemplateFiles(options, force);
}

function syncWorkspaceAgentFiles(options) {
  const sourceCodexDir = path.join(options.templateUrl ? options.templateDir : PACKAGE_ROOT, CODEX_DIR);
  const destSkillsDir = path.join(options.workspace, CODEX_DIR, 'skills');

  for (const skill of MANAGED_SKILLS) {
    const src = path.join(sourceCodexDir, 'skills', skill);
    const dst = path.join(destSkillsDir, skill);

    if (!fs.existsSync(src)) {
      continue;
    }
    if (options.dryRun) {
      console.log(`${paint('dry-run', color.yellow)} sync ${CODEX_DIR}/skills/${skill} into ${options.workspace}`);
      continue;
    }

    fs.mkdirSync(destSkillsDir, { recursive: true });
    fs.rmSync(dst, { recursive: true, force: true });
    fs.cpSync(src, dst, { recursive: true, force: true, preserveTimestamps: true });
  }
}

function quoteEnv(value) {
  return `'${String(value).replace(/'/g, `'\\''`)}'`;
}

function writeWorkspaceSupportScript(options) {
  const envPath = path.join(options.templateDir, 'scripts', 'workspace-support.env');
  const scriptPath = path.join(options.templateDir, 'scripts', 'init-workspace-support.sh');
  const envText = [
    `BUSYBOX_URL=${quoteEnv(SUBMODULES.busybox.url)}`,
    `BUSYBOX_COMMIT=${quoteEnv(SUBMODULES.busybox.commit)}`,
    `QEMU_URL=${quoteEnv(SUBMODULES.qemu.url)}`,
    `QEMU_COMMIT=${quoteEnv(SUBMODULES.qemu.commit)}`,
    ''
  ].join('\n');
  const scriptText = `#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "\${BASH_SOURCE[0]}")" && pwd)"
TEMPLATE_DIR="$(cd "\${SCRIPT_DIR}/.." && pwd)"
# shellcheck source=/dev/null
source "\${SCRIPT_DIR}/workspace-support.env"

SUPPORT_SUBMODULES="\${SUPPORT_SUBMODULES-\${SUBMODULES-busybox qemu}}"
SUBMODULE_DEPTH="\${SUBMODULE_DEPTH-1}"

fetch_commit() {
    local url="$1" dest="$2" commit="$3"
    local -a fetch_args
    mkdir -p "$(dirname "\${dest}")"
    if [[ -d "\${dest}/.git" ]]; then
        git -C "\${dest}" remote set-url origin "\${url}"
    else
        if [[ -e "\${dest}" ]]; then shopt -s nullglob dotglob; existing=("\${dest}"/*); shopt -u nullglob dotglob; else existing=(); fi
        if ((\${#existing[@]} != 0)); then
            echo "Support directory is non-empty and not a git checkout: \${dest}" >&2
            exit 1
        fi
        git init "\${dest}"
        git -C "\${dest}" remote add origin "\${url}"
    fi
    fetch_args=(-C "\${dest}" fetch)
    if [[ -n "\${SUBMODULE_DEPTH}" && "\${SUBMODULE_DEPTH}" != "0" ]]; then
        fetch_args+=(--depth "\${SUBMODULE_DEPTH}")
    fi
    fetch_args+=(origin "\${commit}")
    git "\${fetch_args[@]}"
    git -C "\${dest}" checkout --detach FETCH_HEAD
}

if [[ -z "\${SUPPORT_SUBMODULES}" ]]; then
    echo "SUPPORT_SUBMODULES is empty; skipping support repository updates."
    exit 0
fi

read -r -a repos <<<"\${SUPPORT_SUBMODULES}"
for repo in "\${repos[@]}"; do
    case "\${repo}" in
        busybox)
            fetch_commit "\${BUSYBOX_URL}" "\${TEMPLATE_DIR}/busybox" "\${BUSYBOX_COMMIT}"
            ;;
        qemu)
            fetch_commit "\${QEMU_URL}" "\${TEMPLATE_DIR}/qemu" "\${QEMU_COMMIT}"
            ;;
        *)
            echo "Unsupported support repository: \${repo}" >&2
            exit 1
            ;;
    esac
done
`;
  fs.writeFileSync(envPath, envText, { mode: 0o644 });
  fs.writeFileSync(scriptPath, scriptText, { mode: 0o755 });
}

function initSupportRepos(options) {
  if (options.templateUrl) {
    const script = path.join(options.templateDir, 'scripts', 'init-template.sh');
    run(script, [], {
      dryRun: options.dryRun,
      cwd: options.templateDir,
      env: Object.assign(process.env, {
        REPO_DIR: options.templateDir,
        SUBMODULES: options.supportSubmodules,
        SUBMODULE_DEPTH: options.submoduleDepth,
        ENSURE_BUILD_DEPS: '0'
      })
    });
    return;
  }

  if (!options.supportSubmodules.trim()) {
    return;
  }
  for (const name of options.supportSubmodules.trim().split(/\s+/)) {
    const meta = SUBMODULES[name];
    if (!meta || name === 'linux') {
      throw new Error(`Unsupported support repository: ${name}`);
    }
    fetchCommit(meta.url, path.join(options.templateDir, name), meta.commit, options.submoduleDepth, options.dryRun);
  }
}

function initLinux(options) {
  if (options.linuxDirExplicit) {
    if (!fs.existsSync(options.linuxDir) || !fs.statSync(options.linuxDir).isDirectory()) {
      throw new Error(`--linux-dir does not exist or is not a directory: ${options.linuxDir}`);
    }
    return;
  }
  if (fs.existsSync(options.workspaceLinuxDir) && !isEmptyDir(options.workspaceLinuxDir)) {
    failExisting(`Linux destination is non-empty. Set --linux-dir ${options.workspaceLinuxDir} to use it explicitly.`);
  }
  if (options.linuxBranch) {
    cloneRepo(options.linuxUrl, options.workspaceLinuxDir, options.linuxBranch, options.submoduleDepth, options.dryRun);
  } else {
    fetchCommit(options.linuxUrl, options.workspaceLinuxDir, SUBMODULES.linux.commit, options.submoduleDepth, options.dryRun);
  }
}

function generatedConfig(options) {
  return `${GENERATED_MARKER}
# Workspace-local config. Edit values here; do not edit ${HIDDEN_TEMPLATE_DIR} for kernel experiments.

TEMPLATE_DIR := $(CURDIR)/${HIDDEN_TEMPLATE_DIR}
LINUX_DIR ?= ${options.configLinuxDir}
BUSYBOX_DIR ?= $(TEMPLATE_DIR)/busybox
QEMU_DIR ?= $(TEMPLATE_DIR)/qemu
OUT_DIR ?= $(CURDIR)/out/$(ARCH)
INITRAMFS_DIR ?= $(CURDIR)/out/initramfs/$(ARCH_CANON)
BUSYBOX_OUT_DIR ?= $(CURDIR)/out/busybox/$(ARCH_CANON)
QEMU_BUILD_DIR ?= $(CURDIR)/out/qemu/$(ARCH_CANON)
`;
}

function generatedMakefile() {
  return `${GENERATED_MARKER}
# Workspace wrapper; template implementation lives under ${HIDDEN_TEMPLATE_DIR}.

SHELL := /usr/bin/env bash
TEMPLATE_DIR := $(CURDIR)/${HIDDEN_TEMPLATE_DIR}
CONFIG_FILE := $(CURDIR)/qemu-linux.mk
SUPPORT_SUBMODULES ?= ${DEFAULT_SUPPORT_SUBMODULES}
INIT_SUBMODULES := $(if $(filter undefined,$(origin SUBMODULES)),$(SUPPORT_SUBMODULES),$(SUBMODULES))
.DEFAULT_GOAL := x86

.PHONY: init %
init:
	@if [[ -x "$(TEMPLATE_DIR)/scripts/init-workspace-support.sh" ]]; then \
		SUPPORT_SUBMODULES="$(INIT_SUBMODULES)" SUBMODULE_DEPTH="$(SUBMODULE_DEPTH)" "$(TEMPLATE_DIR)/scripts/init-workspace-support.sh"; \
	else \
		$(MAKE) -f "$(TEMPLATE_DIR)/Makefile" REPO_DIR="$(TEMPLATE_DIR)" CONFIG_FILE="$(CONFIG_FILE)" SUBMODULES="$(INIT_SUBMODULES)" init; \
	fi

%:
	@$(MAKE) -f "$(TEMPLATE_DIR)/Makefile" REPO_DIR="$(TEMPLATE_DIR)" CONFIG_FILE="$(CONFIG_FILE)" "$@"
`;
}

function writeGenerated(pathname, content, dryRun) {
  if (dryRun) {
    console.log(`${paint('dry-run', color.yellow)} write ${pathname}`);
    return;
  }
  if (fs.existsSync(pathname) && !sameGeneratedFile(pathname, content)) {
    failExisting(`Refusing to overwrite changed or unrelated generated file: ${pathname}`);
  }
  fs.writeFileSync(pathname, content);
}

function banner() {
  console.log(paint('\nKernLab', color.bold + color.cyan));
  console.log(paint('Linux-first workspace bootstrap\n', color.dim));
}

function step(message) {
  console.log(`${paint('◆', color.green)} ${message}`);
}

function main() {
  const options = parseArgs(process.argv.slice(2));
  const config = generatedConfig(options);
  const makefile = generatedMakefile();

  banner();
  console.log(`Workspace: ${paint(options.workspace, color.bold)}`);
  console.log(`Linux:    ${options.linuxDirExplicit ? options.linuxDir : options.workspaceLinuxDir}`);
  console.log(`Tooling:  ${options.templateDir}\n`);

  ensureGit();
  if (options.update) {
    validateUpdateWorkspace(options);

    step(options.templateUrl ? 'Updating template tooling' : 'Refreshing packaged template tooling');
    copyTemplateFiles(options, true);

    step('Updating QEMU/BusyBox support repositories');
    initSupportRepos(options);

    step('Refreshing generated workspace wrapper');
    if (!fs.existsSync(path.join(options.workspace, 'qemu-linux.mk'))) {
      writeGenerated(path.join(options.workspace, 'qemu-linux.mk'), config, options.dryRun);
    }
    writeGeneratedOrMarked(path.join(options.workspace, 'Makefile'), makefile, options.dryRun);

    step('Updating workspace agent skills');
    syncWorkspaceAgentFiles(options);

    console.log(`\n${paint('Updated.', color.bold + color.green)} Next steps:`);
    console.log('  make init    # optional: re-run support repo sync');
    console.log('  make run\n');
    return;
  }

  validateWorkspace(options, makefile, config);

  if (!options.dryRun) {
    fs.mkdirSync(options.workspace, { recursive: true });
  }

  step(options.templateUrl ? 'Cloning template tooling' : 'Copying packaged template tooling');
  copyTemplateFiles(options);

  step('Initializing QEMU/BusyBox support repositories');
  initSupportRepos(options);

  step(options.linuxDirExplicit ? 'Using existing Linux source tree' : 'Initializing Linux source tree');
  initLinux(options);

  step('Writing workspace Makefile and qemu-linux.mk');
  writeGenerated(path.join(options.workspace, 'qemu-linux.mk'), config, options.dryRun);
  writeGenerated(path.join(options.workspace, 'Makefile'), makefile, options.dryRun);

  step('Installing workspace agent skills');
  syncWorkspaceAgentFiles(options);

  console.log(`\n${paint('Ready.', color.bold + color.green)} Next steps:`);
  if (path.resolve(options.workspace) !== process.cwd()) {
    console.log(`  cd ${shellWord(options.workspace)}`);
  }
  console.log('  $EDITOR linux/');
  console.log('  $EDITOR qemu-linux.mk   # optional');
  console.log('  make run\n');
}

try {
  main();
} catch (error) {
  console.error(`${paint('error', color.red)} ${error.message}`);
  process.exit(1);
}
