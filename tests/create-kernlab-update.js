#!/usr/bin/env node
'use strict';

const assert = require('assert');
const fs = require('fs');
const os = require('os');
const path = require('path');
const { spawnSync } = require('child_process');

const repoRoot = path.resolve(__dirname, '..');
const workspace = fs.mkdtempSync(path.join(os.tmpdir(), 'kernlab-update-'));
const templateDir = path.join(workspace, '.kernlab');

fs.mkdirSync(path.join(templateDir, 'scripts'), { recursive: true });
fs.writeFileSync(path.join(templateDir, 'Makefile'), 'old\n');
fs.writeFileSync(path.join(templateDir, 'qemu-linux.mk'), 'old\n');
fs.writeFileSync(path.join(templateDir, 'scripts', 'build-kernel.sh'), '#!/bin/sh\n');
fs.writeFileSync(path.join(workspace, 'Makefile'), '# Generated by create-kernlab.\nold wrapper\n');
fs.writeFileSync(path.join(workspace, 'qemu-linux.mk'), '# user config\nLINUX_DIR := custom\n');

try {
  const result = spawnSync(process.execPath, [
    path.join(repoRoot, 'bin', 'create-kernlab.js'),
    '--update',
    '--support-submodules',
    '',
    '--dir',
    workspace
  ], { cwd: repoRoot, encoding: 'utf8' });

  assert.strictEqual(result.status, 0, result.stderr || result.stdout);
  assert.match(result.stdout, /Updated\./);
  assert.strictEqual(
    fs.readFileSync(path.join(workspace, 'qemu-linux.mk'), 'utf8'),
    '# user config\nLINUX_DIR := custom\n'
  );
  assert.match(
    fs.readFileSync(path.join(workspace, 'Makefile'), 'utf8'),
    /Workspace wrapper; template implementation lives under \.kernlab\./
  );
  assert.ok(fs.existsSync(path.join(templateDir, 'scripts', 'workspace-support.env')));
  assert.ok(fs.existsSync(path.join(
    workspace,
    '.codex',
    'skills',
    'kernlab-repo-guardrails',
    'references',
    'linux-kernel-patch-rules.md'
  )));
} finally {
  fs.rmSync(workspace, { recursive: true, force: true });
}
